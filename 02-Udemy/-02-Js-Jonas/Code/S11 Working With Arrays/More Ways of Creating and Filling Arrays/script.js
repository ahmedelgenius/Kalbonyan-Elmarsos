const displayMovements = function (movements, sort = false) {
  // Delete all HTML tags from the container
  containerMovements.innerHTML = '';
  // Sort / unsort movements array based on sort attribute set to true / false (default value)
  const movs = sort ? movements.slice().sort((a, b) => a - b) : movements;

  // Create HTML structure for every item from movements array
  movs.forEach(function (mov, i) {
    // Set movement type based on its value
    const type = mov > 0 ? 'deposit' : 'withdrawal';

    const html = `<div class="movements__row">
        <div class="movements__type movements__type--${type}">${
      i + 1
    } ${type}</div>
        <div class="movements__date">${new Date().getDate()}.${
      new Date().getMonth() + 1
    }.${new Date().getFullYear()} ${new Date().getHours()}:${new Date().getMinutes()}</div>
        <div class="movements__value">${mov}€</div>
      </div>`;

    containerMovements.insertAdjacentHTML('afterbegin', html);
  });
};

